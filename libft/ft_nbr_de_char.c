#include "libft.h"

size_t		ft_nbr_de_char(long nbr)
{
	size_t i;

	i = 0;
	if (nbr == 0)
		return (1);
	while (nbr)
	{
		i++;
		nbr = nbr / 10;
	}
	return (i);
}
